# break, continue and pass

print ("****Example of Break***")

for i in range(1,11):
    if i==5:
        break
    print(i)
    
print ("****Example of Continue***")
    
for i in range(1,11):
    if i==5:
        continue
    print(i)
    
print ("****Example of Pass***")

for i in range(1,11):
    if i==5:
        pass
    print(i)