# String Data Type (Anything inside single quote or double quote is treated as string)

a = "Araannis Mivaan"
print(type(a))

# For multiple line we have to use 3 single quotes(''') or 3 double quotes("""")

b = """Aditya Ranjan Patra.
Araannis Mivaan
Smaranika Panda"""

print(type(b))

# String will work with arithmetic operators 

c = 'adi'
d = 'ranjan'
e = c + d
print (e)

# Below operation is not allowed
f = c-d
print(f)